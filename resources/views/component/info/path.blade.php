
    <p>Thank you for your interest in helping to create and expand a toolkit for humans wanting to improve their inclusive teamwork skills!</p>
    <p>Teamwork is an ongoing practice, and incorporating new ideas and behaviors into that part of our lives requires us to develop a habit of learning.</p>
    <p>The hope of this project is to transform the exceptional variety of educational and inspirational materials already available in the world, into curated collections of learning moments that build on each other to facilitate each other on that journey.</p>
    <p>We're calling these curated collections Paths; each of which has an underlying thesis it aims to teach.</p>

