<p>Paths are made up of learning moments that we're calling Prompts.</p>

<p>To maximize retention, each user of the app is able to set a preference for how frequently they receive new Prompts.</p>

<p>While there is currently a "constant" option to facilitate testing, the suggested frequencies range from one Prompt per day to one Prompt per month, and the default option being three Prompts per week.</p>

<p>The individual interactions that comprise a Prompt, we're calling Segments.</p>

<p>An individual Segment might be a message with an image or link to external media, a suggested exercise for the user to try, or a multiple choice question.</p>
